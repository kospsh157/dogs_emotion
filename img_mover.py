def remove_substrings_from_filenames(filenames, substrings_to_remove):
    removed_file_names = []
    for filename in filenames:
        removed_file_names.append(filename.replace(substrings_to_remove, ''))
    return removed_file_names


def move_specific_images(src_folder, dest_folder, files_to_move):
    # 만약 대상 폴더가 없다면 생성
    if not os.path.exists(dest_folder):
        os.makedirs(dest_folder)

    # 원본 폴더에서 지정된 파일만 옮기기
    for filename in files_to_move:
        src_path = os.path.join(src_folder, filename)
        dest_path = os.path.join(dest_folder, filename)

        # 파일 존재 확인 및 옮기기
        if os.path.exists(src_path):
            shutil.move(src_path, dest_path)
            print(f'다음 파일 {src_path}을 {dest_path}로 옮겼습니다.', )
        else:
            print(f"{filename} does not exist in {src_folder}")


